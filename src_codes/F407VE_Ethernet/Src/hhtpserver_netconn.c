/**
  * @brief serve tcp connection  
  * @param conn: pointer on connection structure 
  * @retval None
  */
void http_server_serve(struct netconn *conn) 
{
  struct netbuf *inbuf;
  char* buf;
  u16_t buflen;
  struct fs_file * file;
	char* path_end = NULL;
  
  /* Read the data from the port, blocking if nothing yet there. 
   We assume the request (the part we care about) is in one netbuf */
  netconn_recv(conn, &inbuf);
  
  if (inbuf != NULL)
  {
    if (netconn_err(conn) == ERR_OK) 
    {
      netbuf_data(inbuf, (void**)&buf, &buflen);
    
			//printf("%s\r\n", buf);
			
      /* Is this an HTTP GET command? (only check the first 5 chars, since
      there are other formats for GET, and we're keeping it very simple )*/
      if ((buflen >=5) && (strncmp(buf, "GET /", 5) == 0))
      {

				// [ HTML pages ]
				if ((strncmp(buf, "GET /cncu_welcome.html", 22) == 0)||(strncmp(buf, "GET / ", 6) == 0))
				{
					file = fs_open("/cncu_welcome.html"); 
          netconn_write(conn, (const unsigned char*)(file->data), (size_t)file->len, NETCONN_NOCOPY);
          fs_close(file);
				}
				else if (strncmp((char const *)buf,"GET /settings?",14)==0)
				{
					file = fs_open("/cncu_settings.html"); 
          netconn_write(conn, (const unsigned char*)(file->data), (size_t)file->len, NETCONN_NOCOPY);
          fs_close(file);
				}
				// Temporary sending login+password via GET request. TODO: Use POST Request
				else if (strncmp((char const *)buf,"GET /?username",14)==0)
				{
					if(strncmp((char const *)buf,"GET /?username=admin&pass=molodec",33)==0)
					{
						file = fs_open("/cncu_mainmenu.html"); 
						netconn_write(conn, (const unsigned char*)(file->data), (size_t)file->len, NETCONN_NOCOPY);
						fs_close(file);
					}
					else
					{
						file = fs_open("/cncu_welcome_err.html"); 
						netconn_write(conn, (const unsigned char*)(file->data), (size_t)file->len, NETCONN_NOCOPY);
						fs_close(file);
					}
				}	
				
				// [ Buttons ]
				else if (strncmp((char const *)buf,"GET /content?",13)==0)
				{
					DynWebPageStr(conn);
				}
				else if (strncmp((char const *)buf,"GET /return?return=mainmenu",27)==0)
				{
					file = fs_open("/cncu_mainmenu.html"); 
					netconn_write(conn, (const unsigned char*)(file->data), (size_t)file->len, NETCONN_NOCOPY);
					fs_close(file);
				}
				
				// [ Files ]
				else if (NULL != (path_end = strstr(&buf[4], " ")))
				{
					char path[40]={0};		
					*path_end = 0;
					strcpy(&path[0], &buf[4]); 
					
					printf("Request:%s\r\n", path);
					
					file = fs_open(path);
					if (file) 
					{
						netconn_write(conn, (const unsigned char*)(file->data), (size_t)file->len, NETCONN_NOCOPY);
						fs_close(file);
					}
					// 404 Load Error page
					else
					{
						fs_close(file);
						file = fs_open("/404.html"); 
						netconn_write(conn, (const unsigned char*)(file->data), (size_t)file->len, NETCONN_NOCOPY);
						fs_close(file);
					}
				}
				else
				{
					file = fs_open("/404.html"); 
					netconn_write(conn, (const unsigned char*)(file->data), (size_t)file->len, NETCONN_NOCOPY);
					fs_close(file);
				}
      }
			else if ((buflen >=5) && (strncmp(buf, "POST /", 6) == 0))
      {
				printf("POST COMMAND\r\n");
			}
    }
  }
  /* Close the connection (server closes in HTTP) */
  netconn_close(conn);
  
  /* Delete the buffer (netconn_recv gives us ownership,
   so we have to make sure to deallocate the buffer) */
  netbuf_delete(inbuf);
}


/**
  * @brief  http server thread 
  * @param arg: pointer on argument(not used here) 
  * @retval None
  */
static void http_server_netconn_thread(void *arg)
{ 
  struct netconn *conn, *newconn;
  err_t err;
  
  /* Create a new TCP connection handle */
  conn = netconn_new(NETCONN_TCP);
  
  if (conn!= NULL)
  {
    /* Bind to port 80 (HTTP) with default IP address */
    err = netconn_bind(conn, NULL, 80);
    
    if (err == ERR_OK)
    {
      /* Put the connection into LISTEN state */
      netconn_listen(conn);
  
      while(1) 
      {
        /* accept any icoming connection */
        netconn_accept(conn, &newconn);
      
        /* serve connection */
        http_server_serve(newconn);
      
        /* delete connection */
        netconn_delete(newconn);
      }
    }
    else
    {
      printf("can not bind netconn");
    }
  }
  else
  {
    printf("can not create netconn");
  }
}

/**
  * @brief  Initialize the HTTP server (start its thread) 
  * @param  none
  * @retval None
  */
void http_server_netconn_init()
{
  sys_thread_new("HTTP", http_server_netconn_thread, NULL, DEFAULT_THREAD_STACKSIZE, WEBSERVER_THREAD_PRIO);
}

